for (const theme of BUILTIN_THEMES) {
  const existing = await client.execute({
    sql: `SELECT id FROM writing_themes WHERE name = ?`,
    args: [theme.name],
  });
  if (existing.rows.length > 0) {
    console.log(`"${theme.name}" already exists — skipping.`);
    continue;
  }
  const now = Date.now();
  await client.execute({
    sql: `INSERT INTO writing_themes (name, definition, is_builtin, created_at, updated_at) VALUES (?, ?, 1, ?, ?)`,
    args: [theme.name, JSON.stringify(theme.definition), now, now],
  });
  console.log(`Inserted "${theme.name}".`);
}

