// One-off: add writing_themes.is_builtin and seed the app's built-in theme
// presets (originally hand-authored as JSON files in src/temp/). Migrations
// are stale, so we apply schema changes directly. Idempotent — safe to
// re-run: the column add is guarded, and each theme is only inserted if no
// row with that exact name already exists.
import { createClient } from '@libsql/client';

const url = process.env.WRITING_DATABASE_URL || 'file:./data/writing.db';
const client = createClient({ url });

async function columnExists(table, col) {
  const res = await client.execute(`PRAGMA table_info(${table})`);
  return res.rows.some((r) => r.name === col);
}

if (!(await columnExists('writing_themes', 'is_builtin'))) {
  await client.execute(`ALTER TABLE writing_themes ADD COLUMN is_builtin INTEGER NOT NULL DEFAULT 0`);
  console.log('Added writing_themes.is_builtin column.');
} else {
  console.log('writing_themes.is_builtin already exists — skipping.');
}

